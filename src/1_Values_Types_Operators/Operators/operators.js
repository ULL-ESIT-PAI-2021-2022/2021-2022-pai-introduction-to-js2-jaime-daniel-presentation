"use strict";

function main() {  
  // EQUALITY OPERATORS:
  // ===, !==, ==, !=
  let x = 10;
  let y = "10";
  console.log(x === y);
  console.log(x !== y);
  console.log(x == y);
  console.log(x != y);

  // OR, AND ASSIGNMENT OPERATORS:
  // ||, &&
  let a = 10 || 20;
  let b = false || 20;
  let c = 10 && 20;
  let d = null && 20;
  console.log(a);
  console.log(b);
  console.log(c);
  console.log(d);
}

if (require.main === module) {
  main();
}
